# Python里支持很多算数运算符
# +加 -减 *乘 /除 **幂运算 //整除  %取余(取模运算)
print(1 + 1)  # 2
print(4 - 2)  # 2
print(3 * 2)  # 6

# 在Python3里，两个整数相除，得到的结果会是一个浮点数
print(6 / 2)  # 3.0 float
print(9 / 2)  # 4.5
print(10 / 3)  # 3.3333333

print(3 ** 3)  # 27
print(81 ** (1 / 2))  # 9.0

print(10 // 3)  # 3  整除，向下取整
print(-5 // 2)  # -3

print(10 % 3)  # 1  取余运算，只取余数部分
print(1928 % 9876)  # 1928
