a = 34
b = 'hello'
c = True
d = ['Allen', 'Trump', 'Jack']
# 使用type内置类可以查看一个变量对应的数据类型
print(type(a))  # <class 'int'>整数型
print(type(b))  # <class 'str'>字符串
print(type(c))  # <class 'bool'>布尔类型
print(type(d))  # <class 'list'>列表类型
print(type(3.14))  # <class 'float'>浮点数
# 在Python里变量没有数据类型，我们所说变量数据类型，其实是变量对应的值的数据类型。
