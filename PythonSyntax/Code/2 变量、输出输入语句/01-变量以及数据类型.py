# a我们称之为变量。使用一个名字，代替一段内容。
a = "hello world"
print(a)

b = 34
print(b)

c = True
print(c)
# c=good 错误
# 数据类型的概念:
# 在Python里数据都有各自对应的类型：

# 数字类型：
print(45)  # int整数类型
print(3.14159)  # float类型
print((-1) ** 0.5)  # complex类型

# 字符串类型：其实就是一段普通的文字
# python里的字符串要求一对单引号或者双引号包裹。
print('good afternoon')
print(45 + 1)
print("45+1")

# 布尔类型：True or False(首字母要大写)
print(4 > 3)
print(1 > 5)

# 列表类型
names = ['Allen', "John", 'Mike']
# 字典类型
person = {'name': 'Allen', 'age': 18, 'address': 'California'}
# 元祖类型
nums = (1, 8, 9, 2, 3, 0)
# 集合类型
x = {9, 'hello', 'hi'}
